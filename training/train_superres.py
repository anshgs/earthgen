def get_tokenized_caption(caption, tokenizer):
    captions = [caption]
    inputs = tokenizer(
        captions, max_length=tokenizer.model_max_length, padding="max_length", truncation=True, return_tensors="pt"
    )
    return inputs.input_ids

def get_image(lrim, unet, vae, text_encoder, tokenizer, args):
    scheduler = DDIMScheduler.from_pretrained(args.pretrained_model_name_or_path, subfolder="scheduler")
    transform = torchvision.transforms.ToPILImage()
    noise_level = torch.tensor([20], dtype=torch.long, device='cuda')
    weight_dtype = torch.float32
    latents = torch.randn((1, 4, 128, 128)).to('cuda')

    prompt_embeds = text_encoder(torch.unsqueeze(get_tokenized_caption("satellite photo", tokenizer)[0], dim=0).to('cuda'))[0]

    scheduler.set_timesteps(30, device='cuda')
    timesteps = scheduler.timesteps

    with torch.no_grad():
        for i, t in enumerate(timesteps):
            
            latent_inp = scheduler.scale_model_input(latents, t)
            latent_inp = torch.cat([latent_inp, torch.unsqueeze(lrim, dim=0)], dim=1)
            noise_pred = unet(latent_inp, t, prompt_embeds, class_labels=noise_level, return_dict=False)[0]

            latents = scheduler.step(noise_pred, t, latents, return_dict=False)[0]
        
        result = vae.decode(latents/vae.config.scaling_factor, return_dict=False)[0]

        return transform(torch.clamp(result[0]*0.5+0.5, 0, 1))


def log_validation(vae, text_encoder, tokenizer, unet, args, accelerator, weight_dtype, epoch):
    logger.info("Running validation... ")

    logvae = accelerator.unwrap_model(vae)
    logunet = accelerator.unwrap_model(unet)
    logtext_encoder = accelerator.unwrap_model(text_encoder)

    if args.seed is None:
        generator = None
    else:
        generator = torch.Generator(device=accelerator.device).manual_seed(args.seed)
        torch.manual_seed(args.seed)

    image_dirs = os.listdir(args.validation_image_paths)

    images = []
    lr_images = []
    
    train_transforms = transforms.Compose(
        [
            transforms.ToTensor(),
            transforms.Normalize([0.5], [0.5]),
        ]
    )
    for i in range(len(image_dirs)):
        lr_image = Image.open(args.validation_image_paths+image_dirs[i]+f'/{args.hr_zoom-2}.jpg').convert("RGB")
        lr_image = lr_image.crop(((lr_image.width*3)//8, (lr_image.width*3)//8, (lr_image.width*5)//8, (lr_image.width*5)//8))
        lr_image = lr_image.crop((0, 0, args.resolution//4, args.resolution//4))


        image = get_image(train_transforms(lr_image).to('cuda'), logunet, logvae, logtext_encoder, tokenizer, args)

        images.append(image)
        lr_images.append(lr_image)

    for tracker in accelerator.trackers:
        if tracker.name == "tensorboard":
            logger.info("Logging images to TensorBoard...")
            np_images = np.stack([np.asarray(img) for img in images])
            tracker.writer.add_images("validation", np_images, epoch, dataformats="NHWC")
            
            np_lr_images = np.stack([np.asarray(img) for img in lr_images])
            tracker.writer.add_images("validation_lr", np_lr_images, epoch, dataformats="NHWC")

        elif tracker.name == "wandb":
            tracker.log(
                {
                    "validation": [
                        wandb.Image(image, caption=f"{i}: {args.validation_prompts[i]}")
                        for i, image in enumerate(images)
                    ]
                }
            )
        else:
            logger.warn(f"image logging not implemented for {tracker.name}")

    torch.cuda.empty_cache()

    return images


